    public int offset(int[] nums) {
        int low = 0;
        int high = nums.length - 1;
        int mid = 0;
        int offset = 0;
        int len = nums.length;
        while (low <= high && nums[low] >= nums[high]) {
            mid = (low + high) / 2;
            if (nums[((mid-1)+len)%len]>nums[mid]){
                return mid;
            }
            if (nums[(mid+1)%len]<nums[mid]){
                return mid+1;
            }
            if (nums[low] > nums[mid]) {
                high = mid - 1;
            } else {
                low = mid + 1;
            }
        }
        return mid;
    }

